package Java_Programming_Elements;

public class AgeofHarry {
    public static void main(String[] args) {
        String name="Harry";
        int birth_year=2000;
        int current_year=2024;
        //Calculate age
        int age = current_year - birth_year;
        //Display the result
        System.out.println(name + " is " + age + " years old.");
    }
}
